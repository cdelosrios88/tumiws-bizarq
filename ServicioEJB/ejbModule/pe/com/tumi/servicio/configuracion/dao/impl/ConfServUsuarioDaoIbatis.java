package pe.com.tumi.servicio.configuracion.dao.impl;

import java.util.List;

import pe.com.tumi.framework.negocio.exception.DAOException;
import pe.com.tumi.framework.negocio.persistencia.dao.impl.TumiDaoIbatis;
import pe.com.tumi.servicio.configuracion.dao.ConfServUsuarioDao;
import pe.com.tumi.servicio.configuracion.domain.ConfServUsuario;

public class ConfServUsuarioDaoIbatis extends TumiDaoIbatis implements ConfServUsuarioDao{
	
	public ConfServUsuario grabar(ConfServUsuario o) throws DAOException {
		ConfServUsuario dto = null;
		try{
			getSqlMapClientTemplate().insert(getNameSpace() + ".grabar", o);
			dto = o;
		}catch(Exception e) {
			throw new DAOException(e);
		}
		return dto;
	}
	
	public ConfServUsuario modificar(ConfServUsuario o) throws DAOException {
		ConfServUsuario dto = null;
		try{
			getSqlMapClientTemplate().update(getNameSpace() + ".modificar", o);
			dto = o;
		}catch(Exception e) {
			throw new DAOException(e);
		}
		return dto;
	}
	
	public List<ConfServUsuario> getListaPorPk(Object o) throws DAOException{
		List<ConfServUsuario> lista = null;
		try{
			lista = (List) getSqlMapClientTemplate().queryForList(getNameSpace() + ".getListaPorPk", o);
		}catch(Exception e) {
			throw new DAOException (e);
		}
		return lista;
	}
	
	public List<ConfServUsuario> getListaPorCabecera(Object o) throws DAOException{
		List<ConfServUsuario> lista = null;
		try{
			lista = (List) getSqlMapClientTemplate().queryForList(getNameSpace() + ".getListaPorCabecera", o);
		}catch(Exception e) {
			throw new DAOException (e);
		}
		return lista;
	}
}