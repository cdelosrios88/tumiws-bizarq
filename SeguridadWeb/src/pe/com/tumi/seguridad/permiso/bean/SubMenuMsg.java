package pe.com.tumi.seguridad.permiso.bean;

public class SubMenuMsg {
	private String tipoCrecimiento;
	private String tipoOrden;
	private String nivel;
	private String tipoFinal;
	private String nombre;
	
	public String getTipoCrecimiento() {
		return tipoCrecimiento;
	}
	public void setTipoCrecimiento(String tipoCrecimiento) {
		this.tipoCrecimiento = tipoCrecimiento;
	}
	public String getTipoOrden() {
		return tipoOrden;
	}
	public void setTipoOrden(String tipoOrden) {
		this.tipoOrden = tipoOrden;
	}
	public String getNivel() {
		return nivel;
	}
	public void setNivel(String nivel) {
		this.nivel = nivel;
	}
	public String getTipoFinal() {
		return tipoFinal;
	}
	public void setTipoFinal(String tipoFinal) {
		this.tipoFinal = tipoFinal;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
}
