package pe.com.tumi.seguridad.domain;

public class FechasAuditoria {
	private String 		strCabecera;
	private Integer 	intValor;
	private String 		strTransaccion;
	
	public String getStrCabecera() {
		return strCabecera;
	}
	public void setStrCabecera(String strCabecera) {
		this.strCabecera = strCabecera;
	}
	public Integer getIntValor() {
		return intValor;
	}
	public void setIntValor(Integer intValor) {
		this.intValor = intValor;
	}
	public String getStrTransaccion() {
		return strTransaccion;
	}
	public void setStrTransaccion(String strTransaccion) {
		this.strTransaccion = strTransaccion;
	}
	
}
